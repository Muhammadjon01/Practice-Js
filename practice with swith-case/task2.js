function f(x){
    switch (x) {
        case 1:
            console.log("bad")
            break;
        case 2:
            console.log("unsatisfactory")
        break;
        case 3:
            console.log("satisfactory")
        break;
        case 4:
            console.log("good")
        break;
        case 5:
            console.log("well")
        break;
        default:
            break;
    }
}

f(2)