function f(x){
    switch (x) {
        case 1:
            console.log("+")
        break;
        case 2:
            console.log("-")
        break;
        case 3:
            console.log("*")
        break;
        case 4:
            console.log("/")
        break;
    
        default:
            break;
    }
}

f(2)