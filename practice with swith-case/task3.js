function f(x){
    switch (x) {
        case 1:
            console.log("Jan-winter")
        break;
        case 2:
            console.log("Feb-winter")
        break;
        case 3:
            console.log("Mar-spring")
        break;
        case 4:
            console.log("Apr-spring")
        break;
        case 5:
            console.log("May-spring")
        break;
        case 6:
            console.log("June-summer")
        break;
        case 7:
            console.log("July-summer")
        break;
        case 8:
            console.log("Aug-summer")
        break;
        case 9:
            console.log("Sep-aut")
        break;
        case 10:
            console.log("Oct-Aut")
        break;
        case 11:
            console.log("Nov-Aut")
        break;
        case 12:
            console.log("Dec-winter")
        break;
    
        default:
            break;
    }
}

f(10)